//사칙 연산 함수를 만들어 출력
function plu(a, b){
  // +의 결과값 반환
  console.log(a + b);
  return;
}
function min(a, b){
  // -의 결과값 반환
  console.log(a - b);
  return;
}
function mul(a,b){
  console.log(a * b);
  return;
  // *의 결과값 반환
}
function div(a,b){
  console.log(a / b);
  return;
  // /의 결과값 반환
}

plu (1,3); //4
min (3,2); //1
mul (2,2); //4
div (4,2); //2